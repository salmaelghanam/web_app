<section class="pro">
<h2 class="headers"> What does underweight mean? </h2>
<p>
    Being underweight means that your weight is below the healthy weight range — the weight that <br />
    research has shown you need to be if your body is to work properly.<br />
    Being outside this range, whether overweight or underweight, <br />
    can affect your immunity, fertility, mental wellbeing and other aspects of health.
</p>
</section>
<section class="pro">
<h2 class="headers">What are the symptoms of being underweight?</h2>
<p>
    If you are underweight you might find you get sick often, or feel very tired. <br/>
    That may be because you are not getting the right nutrients you need from your diet.<br/>
    You may also find your hair gets thinner or falls out, your skin gets very dry and your teeth are affected.<br/>

    <br>Children who are underweight may not grow as expected for their age.<br/>

   <br> Being underweight can lead to health problems including:
    <ul>
     <li>osteoporosis</li>
     <li>infections, which are harder to fight off</li>
     <li>irregular periods in females, or menstruation may stop altogether</li>
     <li>pregnancy problems, such as difficulty getting pregnant or a higher chance of preterm labour</li>
     <li>heart problems</li>
    </ul>
    Being underweight can be especially dangerous for older people<br />
    since it increases the risk of breaking a bone and becoming ill.  
</p>
</section>
<section class="pro">
<h2 class="headers">What are the risks of being underweight?</h2>
<p>
    If you are underweight, you may be at greater risk of certain health conditions,<br/>
    including malnutrition, osteoporosis, decreased muscle strength, hypothermia and lowered immunity.<br/>
    Underweight is also known to shorten people’s lives, due to early death.<br/>
   <br> Underweight females have less chance of becoming pregnant<br/> 
    than those who are a healthy weight.
</p>
</section>
<section class="pro">
<h2 class="headers">How can I gain weight safely?</h2>
<p>
    <span>you are underweight, it is important to eat a variety of foods that give you the nutrition you need.<br />
    You should make sure you eat foods with enough energy to help you gain weight, protein to repair your body <br />
    and build your muscles, and vitamins and minerals to keep your body cells functioning well.</span><br />

    <br>The aim is to gain weight gradually by eating healthy foods.<br />
     Even if you are underweight, try to avoid foods with a lot of added sugar,<br />
     fat and salt, like cakes, takeaway foods and sugary drinks.<br />
</p>
<p>
    You can put on weight by eating small meals frequently throughout the day.<br />
    Try to snack on healthy, high-energy foods like cheese, nuts, milk-based smoothies and dried fruit.<br /> 
    <br>More tips for gaining weight safely include:<br />
    <ul>
    <li>using full cream milk in coffee, tea or with meals</li>
    <li>using healthy fats like olive oil or avocado in your meals</li>
    <li>sprinkling some grated cheese on cooked food</li>
    <li>adding milk powder to soups, stews and drinks</li>
    <li>adding protein powder to milkshakes</li>
    <li>replacing black tea or coffee with milky drinks</li>
    <li>doing some light exercise to increase your appetite</li>
    </ul>
</p>
